    private void BotonBuquedaAvanzadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuquedaAvanzadaActionPerformed
        
        switch (ComboBoxBusquedaAvanzada.getSelectedIndex()) {
            case 0://Buscar Codigo
                tableInventario.setInventarios(controladorInventarioAvazado.obetenerProdctoInventarioCodgo(txtBusquedaAvanzada.getText()));
                tableInventario.fireTableDataChanged();
                 break;
            case 1:// Descripcion
                tableInventario.setInventarios(controladorInventarioAvazado.obetenerProdctoInventarioCompraSinIva(txtBusquedaAvanzada.getText()));
                tableInventario.fireTableDataChanged();
                break;
            case 2:
               tableInventario.setInventarios(controladorInventarioAvazado.obetenerProdctoInventarioCompraSinIva(txtBusquedaAvanzada.getText()));
                tableInventario.fireTableDataChanged();
                break;    
              case 3:
                tableInventario.setInventarios(controladorInventarioAvazado.obetenerProdctoInventarioCompraConIva(txtBusquedaAvanzada.getText()));
                tableInventario.fireTableDataChanged();
                break;
            // si selecciona cantidad
            case 4:
                tableInventario.setInventarios(controladorInventarioAvazado.obetenerProdctoInventarioCantidad(txtBusquedaAvanzada.getText()));
                tableInventario.fireTableDataChanged();
                break;   
            default:
                throw new AssertionError();
        }
    }//GEN-LAST:event_BotonBuquedaAvanzadaActionPerformed


